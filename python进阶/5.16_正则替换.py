"""
正则替换新旧两种方式
"""
# 需求，利用正则替换将哈，嘿，呵，桀替换成其他字符
# 1. 导包
import re

# 2. 替换
s = "开心就要大声笑：哈哈，呵呵，嘿嘿，桀桀，哇哇"

# 旧版:               正则规则          替换字符   被替换字符串
result = re.compile("哈|呵|嘿|桀").sub("*", s)

# 3. 输出
print(result)
print('-' * 50)

# 新版：                 正则规则       替换字符   被替换字符串
result = re.sub("哈|呵|嘿|桀", "12", s)
print(result)
